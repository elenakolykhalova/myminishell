#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*dst;
	int		len;
	int		i;

	len = 0;
	while (s[len])
	{
		len++;
	}
	dst = (char *)malloc(sizeof(char) * (len + 1));
	if (!dst)
		return (NULL);
	i = 0;
	while (i < len)
	{
		dst[i] = s[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}
