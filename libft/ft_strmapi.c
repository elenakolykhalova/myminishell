#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	len;
	unsigned int	i;
	char			*output;

	if (!s || !f)
		return (0);
	len = ft_strlen((char *)s);
	output = malloc(sizeof(char) * (len + 1));
	if (!output)
		return (0);
	i = 0;
	while (s[i])
	{
		output[i] = f(i, s[i]);
		i++;
	}
	output[i] = '\0';
	return (output);
}
